package javaConcepts;

public class IntegerCache {
	public static void main(String[] args) {
		/**
		 * Integer Caching
		 * 
		 * -128 to 127
		 */

		Integer num1 = 100;
		Integer num2 = 100;

		if (num1 == num2)
			System.out.println("both numbers are equal");
		else
			System.out.println("both numbers are not equal");
	}
}
