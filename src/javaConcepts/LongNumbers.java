package javaConcepts;

public class LongNumbers {
	public static void main(String[] args) {

		long longNum = 1000 * 60 * 60 * 24 * 365;
		long longNumL = 1000 * 60 * 60 * 24 * 365L;
		System.out.println(longNum);
		System.out.println(longNumL);
		// 1471228928
		// 3153600000 --36 Bits-Decimel Number
		// Max Limit Of 32 Bit Int : 2147833647
		// 11101010111101100010010110000000000

		// 1010111101100010010110000000000 --->1471228928

	}

}
