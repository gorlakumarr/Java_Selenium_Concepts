package javaConcepts;

public class Final {
	public static void main(String[] args) {

		final int i = 10;
		/**
		 * Is A Modifier:-
		 * 
		 * 1.Classes : Cant extend this class. i.e we cant create child class for that
		 * class
		 * 
		 * 2.Methods: we cant override that methods in child class
		 * 
		 * 3.Variables:it will become contant and we cant perfrom reassignment for that
		 * variable
		 * 
		 * Constant Value....
		 * 
		 * Prevent Inheritence
		 */

		System.out.println(i);
	}

}
